    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow(); // Mendapatkan baris yang diklik
        if (row != -1) { // Pastikan ada baris yang dipilih
        // Mengambil data dari tabel berdasarkan baris yang dipilih
        String KodeBarang = jTable1.getValueAt(row, 0).toString();
        String Nama = jTable1.getValueAt(row, 1).toString();
        String HargaBeli = jTable1.getValueAt(row, 2).toString();
        String Harga = jTable1.getValueAt(row, 3).toString();
        String Jumlah = jTable1.getValueAt(row, 4).toString();
        String KodeJenis = jTable1.getValueAt(row, 5).toString();
        
        // Mengisi data ke TextField
        jTextField1.setText(KodeBarang);
        jTextField2.setText(Nama);
        jTextField3.setText(HargaBeli);
        jTextField4.setText(Harga);
        jTextField5.setText(Jumlah);
        jTextField6.setText(KodeJenis);
        
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jTable1MouseClicked


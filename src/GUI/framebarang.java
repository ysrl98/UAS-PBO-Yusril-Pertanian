    private void tampilkanData() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("KodeBarang");
    model.addColumn("NamaBarang");
    model.addColumn("HargaBeli");
    model.addColumn("Harga");
    model.addColumn("Jumlah");
    model.addColumn("KodeJenis");

    try {
        // Ambil data dari tabel barang
        ResultSet rs = con.getData("SELECT * FROM barang");
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("KodeBarang"),
                rs.getString("NamaBarang"),
                rs.getString("HargaBeli"),
                rs.getString("Harga"),
                rs.getString("Jumlah"),
                rs.getString("KodeJenis")
            });
        }
        jTable1.setModel(model); // Set model tabel dengan data baru
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Gagal memuat data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}

    
    

